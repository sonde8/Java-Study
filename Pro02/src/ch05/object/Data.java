package ch05.object;

public class Data {		// Data a=new Data()
	public int x;
	public int y;
	
	// set
	public void setX(int a) {	// a.setX(10)
		x=a;	// x=a=10
	}
	
	public void setY(int b) {	// a.setY(20)
		y=b;	// y=b=20
	}
	
	// get
	public int getX() {
		return x;	// 10
	}
	
	public int getY() {
		return y;	// 20
	}
}
